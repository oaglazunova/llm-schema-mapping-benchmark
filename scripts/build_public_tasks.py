from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_DIR = REPO_ROOT / "src"

if str(SRC_DIR) not in sys.path:
    sys.path.insert(0, str(SRC_DIR))

from lsmbench.sources.fhir.fhir_to_task import public_fhir_tasks
from lsmbench.sources.openapi.openapi_to_task import public_openapi_tasks


def write_json(path: Path, obj: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(obj, f, indent=2, ensure_ascii=False)


def ensure_unique_task_ids(task_specs: list[dict[str, Any]]) -> None:
    ids = [spec["task_id"] for spec in task_specs]
    duplicates = sorted({tid for tid in ids if ids.count(tid) > 1})
    if duplicates:
        raise ValueError(f"Duplicate public task IDs found: {duplicates}")


def build_public_tasks() -> list[str]:
    """
    Materialize public benchmark tasks into:
    - benchmark/tasks/public/
    - benchmark/fixtures/public/
    - benchmark/gold/{matches,plans,invariants}/
    """
    task_specs = public_fhir_tasks() + public_openapi_tasks()
    ensure_unique_task_ids(task_specs)

    task_refs: list[str] = []

    for spec in task_specs:
        task_id = spec["task_id"]

        input_path = REPO_ROOT / "benchmark" / "fixtures" / "public" / f"{task_id}_input.json"
        expected_path = REPO_ROOT / "benchmark" / "fixtures" / "public" / f"{task_id}_expected.json"
        matches_path = REPO_ROOT / "benchmark" / "gold" / "matches" / f"{task_id}_matches.json"
        plan_path = REPO_ROOT / "benchmark" / "gold" / "plans" / f"{task_id}_plan.json"
        invariants_path = REPO_ROOT / "benchmark" / "gold" / "invariants" / f"{task_id}_invariants.json"
        task_path = REPO_ROOT / "benchmark" / "tasks" / "public" / f"{task_id}_task.json"

        write_json(input_path, spec["input_fixture"])
        write_json(expected_path, spec["expected_fixture"])
        write_json(matches_path, spec["matches"])
        write_json(plan_path, spec["plan"])
        write_json(invariants_path, spec["invariants"])

        task_obj = {
            "task_id": task_id,
            "title": spec["title"],
            "split": "public",
            "difficulty": spec["difficulty"],
            "source_family": spec["source_family"],
            "target_entity": spec["target_entity"],
            "task_text": spec["task_text"],
            "source_schema": spec["source_schema"],
            "target_schema": spec["target_schema"],
            "fixture_refs": {
                "input": f"benchmark/fixtures/public/{task_id}_input.json",
                "expected": f"benchmark/fixtures/public/{task_id}_expected.json",
            },
            "gold_refs": {
                "matches": f"benchmark/gold/matches/{task_id}_matches.json",
                "plan": f"benchmark/gold/plans/{task_id}_plan.json",
                "invariants": f"benchmark/gold/invariants/{task_id}_invariants.json",
            },
            "tags": spec["tags"],
            "downstream_checks": spec.get("downstream_checks", []),
            "notes": [
                "Public anchor task.",
                "Hand-built public task generated by build_public_tasks.py.",
            ],
        }

        write_json(task_path, task_obj)
        task_refs.append(f"benchmark/tasks/public/{task_id}_task.json")

    return task_refs


def build_public_task_set(task_refs: list[str]) -> None:
    """
    Build benchmark/task_sets/public_anchor_v1.json

    Split tasks automatically by declared difficulty.
    """
    task_ids = [Path(ref).stem.replace("_task", "") for ref in task_refs]

    easy: list[str] = []
    medium: list[str] = []
    hard: list[str] = []

    for ref in task_refs:
        task_path = REPO_ROOT / ref
        with open(task_path, "r", encoding="utf-8") as f:
            task = json.load(f)

        tid = task["task_id"]
        difficulty = task["difficulty"]

        if difficulty == "easy":
            easy.append(tid)
        elif difficulty == "medium":
            medium.append(tid)
        elif difficulty == "hard":
            hard.append(tid)
        else:
            raise ValueError(f"Unknown difficulty '{difficulty}' in {tid}")

    out = {
        "name": "public_anchor_v1",
        "version": "0.1.0",
        "tasks": task_refs,
        "splits": {
            "easy": sorted(easy),
            "medium": sorted(medium),
            "hard": sorted(hard),
        },
        "notes": [
            "Public anchor split.",
            "Currently contains FHIR-to-GameBus interoperability tasks.",
            "Generic OpenAPI-style public tasks can be added later without changing this builder.",
        ],
    }

    write_json(REPO_ROOT / "benchmark" / "task_sets" / "public_anchor_v1.json", out)


def build_pilot_task_set(public_task_refs: list[str]) -> None:
    """
    Update benchmark/task_sets/pilot_v1.json so it combines:
    - existing GameBus tasks
    - generated public tasks
    """
    gamebus_task_dir = REPO_ROOT / "benchmark" / "tasks" / "gamebus"

    gamebus_refs = sorted(
        str(path.relative_to(REPO_ROOT))
        for path in gamebus_task_dir.glob("GB_*_task.json")
    )

    out = {
        "name": "pilot_v1",
        "version": "0.1.0",
        "tasks": gamebus_refs + public_task_refs,
        "notes": [
            "Pilot benchmark combining GameBus semi-real anchor tasks with public anchor tasks."
        ],
    }

    write_json(REPO_ROOT / "benchmark" / "task_sets" / "pilot_v1.json", out)


def main() -> int:
    task_refs = build_public_tasks()
    build_public_task_set(task_refs)
    build_pilot_task_set(task_refs)

    print(f"Wrote {len(task_refs)} public tasks.")
    for ref in task_refs:
        print(f"  - {ref}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())