from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_DIR = REPO_ROOT / "src"

if str(SRC_DIR) not in sys.path:
    sys.path.insert(0, str(SRC_DIR))

from lsmbench.benchmark.task_builder import write_json
from lsmbench.generation.synthetic_task_generator import native_synthetic_specs


def _task_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "tasks" / "synthetic" / "native" / f"{task_id}_task.json"


def _fixture_input_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "fixtures" / "synthetic" / f"{task_id}_input.json"


def _fixture_expected_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "fixtures" / "synthetic" / f"{task_id}_expected.json"


def _matches_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "gold" / "matches" / f"{task_id}_matches.json"


def _plan_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "gold" / "plans" / f"{task_id}_plan.json"


def _invariants_path(task_id: str) -> Path:
    return REPO_ROOT / "benchmark" / "gold" / "invariants" / f"{task_id}_invariants.json"


def build_synthetic_tasks() -> list[str]:
    specs = native_synthetic_specs()
    task_refs: list[str] = []

    for spec in specs:
        task_id = spec["task_id"]

        write_json(_fixture_input_path(task_id), spec["input_fixture"])
        write_json(_fixture_expected_path(task_id), spec["expected_fixture"])
        write_json(_matches_path(task_id), spec["matches"])
        write_json(_plan_path(task_id), spec["plan"])
        write_json(_invariants_path(task_id), spec["invariants"])

        task_obj = {
            "task_id": task_id,
            "title": spec["title"],
            "split": "synthetic",
            "difficulty": spec["difficulty"],
            "source_family": spec["source_family"],
            "target_entity": spec["target_entity"],
            "task_text": spec["task_text"],
            "source_schema": spec["source_schema"],
            "target_schema": spec["target_schema"],
            "fixture_refs": {
                "input": "../../../fixtures/synthetic/" + f"{task_id}_input.json",
                "expected": "../../../fixtures/synthetic/" + f"{task_id}_expected.json",
            },
            "gold_refs": {
                "matches": "../../../gold/matches/" + f"{task_id}_matches.json",
                "plan": "../../../gold/plans/" + f"{task_id}_plan.json",
                "invariants": "../../../gold/invariants/" + f"{task_id}_invariants.json",
            },
            "tags": spec["tags"],
            "downstream_checks": spec.get("downstream_checks", []),
            "notes": [
                "Synthetic native task.",
                "Generated by scripts/generate_synthetic_tasks.py.",
            ],
        }

        write_json(_task_path(task_id), task_obj)
        task_refs.append(f"benchmark/tasks/synthetic/native/{task_id}_task.json")

    return task_refs


def build_task_set(task_refs: list[str]) -> None:
    easy, medium, hard = [], [], []

    for ref in task_refs:
        task_path = REPO_ROOT / ref
        with open(task_path, "r", encoding="utf-8") as f:
            task = json.load(f)

        tid = task["task_id"]
        diff = task["difficulty"]

        if diff == "easy":
            easy.append(tid)
        elif diff == "medium":
            medium.append(tid)
        elif diff == "hard":
            hard.append(tid)
        else:
            raise ValueError(f"Unknown difficulty {diff!r} in {tid}")

    out = {
        "name": "synthetic_v1",
        "version": "0.1.0",
        "tasks": task_refs,
        "splits": {
            "easy": sorted(easy),
            "medium": sorted(medium),
            "hard": sorted(hard),
        },
        "notes": [
            "Native synthetic split.",
            "Task families are intentionally simple in v1.",
        ],
    }

    write_json(REPO_ROOT / "benchmark" / "task_sets" / "synthetic_v1.json", out)


def build_metadata(task_refs: list[str]) -> None:
    primitive_catalog = {
        "native_families": [
            "flat_record_normalization",
            "daily_numeric_summary",
            "event_normalization",
            "embedded_json_parsing",
            "key_value_extraction",
            "grouped_aggregation",
            "filter_then_map",
        ]
    }
    write_json(REPO_ROOT / "benchmark" / "metadata" / "primitive_catalog.json", primitive_catalog)

    difficulty_csv = REPO_ROOT / "benchmark" / "metadata" / "difficulty_index.csv"
    difficulty_csv.parent.mkdir(parents=True, exist_ok=True)

    rows = ["task_id,difficulty,split,source_family,target_entity"]
    for ref in task_refs:
        task_path = REPO_ROOT / ref
        with open(task_path, "r", encoding="utf-8") as f:
            task = json.load(f)
        rows.append(
            f"{task['task_id']},{task['difficulty']},{task['split']},{task['source_family']},{task['target_entity']}"
        )

    difficulty_csv.write_text("\n".join(rows) + "\n", encoding="utf-8")

    log_path = REPO_ROOT / "benchmark" / "metadata" / "synthetic_generation_log.jsonl"
    with open(log_path, "w", encoding="utf-8") as f:
        for ref in task_refs:
            task_path = REPO_ROOT / ref
            task = json.loads(task_path.read_text(encoding="utf-8"))
            f.write(json.dumps({
                "task_id": task["task_id"],
                "generator": "native_synthetic_specs",
                "difficulty": task["difficulty"],
                "source_family": task["source_family"],
                "target_entity": task["target_entity"],
            }) + "\n")


def main() -> int:
    task_refs = build_synthetic_tasks()
    build_task_set(task_refs)
    build_metadata(task_refs)

    print(f"Wrote {len(task_refs)} synthetic tasks.")
    for ref in task_refs:
        print(f"  - {ref}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())